package ProgramDesignII.Ch4;

public class MultiWayIfElse {
    public static void main(String[] args) {
        int a = 10;

        if(a > 10){
            System.out.println("a>10");
        }else if(a > 1){
            System.out.println("a>1");
        }else if(a > 2){
            System.out.println("a>2");
        }
    }
}
