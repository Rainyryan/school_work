package JavaTutorialForBeginners;

public class escapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello \"Ryan\""); // \ = printNextCharacter
        System.out.println("c: \nWindows\\..."); // \n = newLine 
        System.out.println("Hello\tRyan"); // \t = tab
    }
}
